package webshop.beans;

/**
 *
 * @author Lijeposava
 */
public class User {

    private int IDUser;
    private String firstName;
    private String lastName;
    private String street;
    private String postalNumber;
    private String town;
    private String email;
    private String password;
    private boolean isAdmin;
    private int houseNumber;

    public User() {

    }

    @Override
    public String toString() {
        return "User{" + "IDUser=" + IDUser + ", firstName=" + firstName + ", lastName=" + lastName + ", street=" + street + ", postalNumber=" + postalNumber + ", town=" + town + ", email=" + email + ", password=" + password + ", isAdmin=" + isAdmin + ", houseNumber=" + houseNumber + '}';
    }

    public int getHouseNumber() {
        return houseNumber;
    }

    public void setHouseNumber(int houseNumber) {
        this.houseNumber = houseNumber;
    }

    public String getPostalNumber() {
        return postalNumber;
    }

    public void setPostalNumber(String postalNumber) {
        this.postalNumber = postalNumber;
    }

    public int getIDUser() {
        return IDUser;
    }

    public void setIDUser(int IDUser) {
        this.IDUser = IDUser;
    }

    public boolean isIsAdmin() {
        return isAdmin;
    }

    public void setIsAdmin(boolean isAdmin) {
        this.isAdmin = isAdmin;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getTown() {
        return town;
    }

    public void setTown(String town) {
        this.town = town;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

}
